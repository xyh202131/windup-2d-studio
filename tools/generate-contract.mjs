import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const source = resolve(root, 'contracts/studio.v1.json')
const contract = JSON.parse(await readFile(source, 'utf8'))
const json = JSON.stringify(contract, null, 2)
const ts = `// Generated by tools/generate-contract.mjs. Do not edit.\nexport const STUDIO_CONTRACT = ${json} as const\n`
const py = `\"\"\"Generated by tools/generate-contract.mjs. Do not edit.\"\"\"\n\nSTUDIO_CONTRACT = ${JSON.stringify(contract, null, 2).replaceAll('true', 'True').replaceAll('false', 'False').replaceAll('null', 'None')}\n`
const targets = [
  [resolve(root, 'frontend/src/generated-contract.ts'), ts],
  [resolve(root, 'backend/app/generated_contract.py'), py],
]
const check = process.argv.includes('--check')
let drift = false
for (const [path, content] of targets) {
  if (check) {
    let current = ''
    try { current = await readFile(path, 'utf8') } catch { /* missing */ }
    if (current !== content) { console.error(`Contract drift: ${path}`); drift = true }
  } else {
    await writeFile(path, content, 'utf8')
  }
}
if (drift) process.exit(1)

